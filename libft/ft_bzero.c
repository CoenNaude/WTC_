#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			x;
	unsigned char	*str;

	x = 0;
	str = (unsigned char *)s;
	while (x < n && n > 0)
	{
		str[x] = 0;
		x++;
	}
}
