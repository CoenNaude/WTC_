#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t x;

	x = 0;
	while (src[x] != '\0')
	{
		dest[x] = src[x];
		x++;
	}
	dest[x] = '\0';
	return (dest);
}
