#include "libft.h"

size_t		ft_wordlen(const char *s, char c)
{
	size_t	x;
	size_t	len;

	x = 0;
	len = 0;
	while (s[x] == c)
		x++;
	while (s[x] != c && s[x++])
		len++;
	return (len);
}
