#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		x;
	int		y;

	x = ft_strlen(s);
	y = 0;
	while (x >= y)
	{
		if (s[x - y] == c)
		{
			return ((char *)&s[x - y]);
		}
		y++;
	}
	return (NULL);
}
