#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int a;
	int b;

	a = octet;
	b = 128;
	while(b > 0)
	{
		if(a >= b)
		{
			write(1, "1", 1);
			a -= b;
		}
		else
			write(1, "0", 1);
		b /= 2;
	}
}
