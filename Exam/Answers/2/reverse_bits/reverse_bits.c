unsigned char	reverse_bits(unsigned char octet)
{
	unsigned char a;
	unsigned char b;

	a = octet >> 4;
	b = (octet << 4) >> 4;
	return (((((a & 8) >> 3) | ((a & 4) >> 1) | ((a & 2) << 1) | ((a & 1) << 3)) | 
			 (((b & 8) >> 3) | ((b & 4) >> 1) | ((b & 2) << 1) | ((b & 1) << 3)) << 4));
}
